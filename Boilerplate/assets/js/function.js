function sumNumbers(num1, num2) {
    return num1 + num2;
}

function substractNumber(num1, num2) {
    return num1 - num2;
}

function divisionNumber(num1, num2) {
    return num1 / num2;
}

function multiplicationNumber(num1, num2) {
    return num1 * num2;
}

export {sumNumbers, substractNumber, divisionNumber, multiplicationNumber}